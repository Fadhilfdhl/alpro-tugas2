# Tipe data integer (int)
angka_bulat = 10

# Tipe data float (float)
angka_desimal = 3.14

# Tipe data string (str)
teks = "HalloWorld"

# Tipe data boolean (bool)
benar = True
salah = False

# Tipe data list
daftar_angka = [1, 2, 3, 4, 5]

# Tipe data tuple
tuple_angka = (1, 2, 3, 4, 5)

# Tipe data dictionary
data_mahasiswa = {'nama': 'Fadhil', 'usia': 18, 'kota': 'Bogor'}

# Tipe data set
set_angka = {1, 2, 3, 4, 5}

# Menampilkan nilai dari setiap tipe data
print("Nilai dari angka_bulat:", angka_bulat)
print("Nilai dari angka_desimal:", angka_desimal)
print("Nilai dari teks:", teks)
print("Nilai dari benar:", benar)
print("Nilai dari salah:", salah)
print("Nilai dari daftar_angka:", daftar_angka)
print("Nilai dari tuple_angka:", tuple_angka)
print("Nilai dari data_mahasiswa:", data_mahasiswa)
print("Nilai dari set_angka:", set_angka)
